package Students_db;

public class Students {
    int roll;
    String name;
    int age;
    String course;
    int maths;
    int chemistry;
    int physics;

    public Students(int roll, String name, int age, String course, int maths, int chemistry, int physics){
        this.roll = roll;
        this.name = name;
        this.age = age;
        this.course = course;
        this.maths = maths;
        this.physics = physics;
        this.chemistry = chemistry;
    }

}
